// volume of a cuboid
// Bob needs a fast way to calculate the volume of a cuboid with three values: the length, width and height of the cuboid. Write a function to help Bob with this calculation.


//**My solution 
class Kata {
  static getVolumeOfCuboid(l, w, h) {
    return l * w * h;
  }
};

